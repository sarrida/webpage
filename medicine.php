                    <p style="text-indent:2.5em;"> At the outset, it is necessary to define Thai medicine. According to
                        Practice of the Art of Healing Act, Thai medicine is examination, diagnosis, cure or prevention
                        of disease by means of Thai traditional medicine. The process of learning and teaching Thai
                        medicine includes both theory and practice sessions. Theory sessions deal with diagnosis based
                        on Thai traditional medicine with reference to scriptures, such as the Takkasila Scripture, the
                        Chantasat Scripture and the Pathomjinda Scripture. Further, the causes of a disease, its
                        symptoms and its pathology are compared as between Thai traditional medicine and modern
                        medicine. Finally, treatment, the formulation of Thai medicinal recipes, the prescribing of Thai
                        medicine and referral are discussed in these sessions. Practice sessions emphasize practical
                        considerations. Students undertake field work by seeing patients in the community. They also
                        learn from Thai traditional doctors working in villages and study and observe how doctors
                        practicing modern medicine give diagnoses in hospitals. They also observe children’s behavior
                        and development at nurseries. Last but not least, they undertake internships at different
                        hospitals all over the country. </p>
                    <p style="text-indent:2.5em;"> Graduates are expected to be able to apply what they have learned of
                        Thai traditional medicine to the fullest extent and treat patients effectively based on the
                        principles and objectives of Thai traditional medicine.</p>
                    <p style="text-indent:2.5em;"><strong> Strength</strong> Thai traditional medicine is a source of
                        national pride
                        which has been used to treat patients for centuries. Even though these days, many Thai people
                        prefer modern medicine, Thai traditional medicine is still useful and appropriate for the social
                        and economic conditions existing in Thailand.</p>
                    <p style="text-indent:2.5em;"><strong>How is it different from modern medicine?</strong> The
                        explanatory model
                        is
                        different. Modern medicine explains diseases as far as possible based on science. Modern
                        medicine has its origins in germ theory which sought to explain many abnormalities in the body
                        as resulting from micro-organisms. When treating patients, doctors in modern medicine focus on
                        abnormality in the body, such as abnormal or infected organs and cells or the presence of
                        abnormal chemicals, to diagnose what illness patients have or what is not normal in the body.
                        Afterwards, they prescribe medicine or repair organs by, for example, stitching a wound,
                        incising a cyst, prescribing medication to kill germs or stop the production of chemicals in the
                        body and to repair abnormal cells. They sometimes forget that some medicines cause excruciating
                        pain. At times physical pain or illness is relieved or treated, but what remains is mental pain
                        and distress.</p>
                    <p style="text-indent:2.5em;"> Students of Thai traditional medicine have to study and gain basic
                        knowledge about the cause of illness, pathophysiology and the approach to treatment adopted by
                        modern medicine. Thai traditional medicine also takes into account the relationship between
                        elements of the body, season, the patient’s age and behavior, and the time and place, viewing
                        them as integrally interconnected. Thai traditional medicine, therefore, diagnoses and treats
                        disorders based on an approach which places emphasis on the patient and their circumstances, for
                        example, their families, communities and environment. The services given focus on individual
                        patients combined with the physical, biological, psychological and social environment.</p>
